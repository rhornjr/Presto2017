
namespace PrestoCommon.Entities
{
    /// <summary>
    /// Custom Variable
    /// </summary>
    public class CustomVariable : ActivatableEntity
    {
        /// <summary>
        /// Gets or sets the key.
        /// </summary>
        /// <value>
        /// The key.
        /// </value>
        public string Key { get; set; }

        /// <summary>
        /// Gets or sets the value.
        /// </summary>
        /// <value>
        /// The value.
        /// </value>
        public string Value { get; set; }
    }
}
